package ru.ilukhinayana.hw2calcapp.util;

public class Calculator {

    public static double sum (double num1, double num2){ // метод Сложения
        double summi = num1 + num2;
        return summi;
    }

    public static double subtract (double num1, double num2){ // метод Вычитания
        return num1 - num2;
    }

    public static double multiply (double num1, double num2){ // метод Умножения
        return num1 * num2;
    }

}
